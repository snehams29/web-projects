function fun(){
    var copyText = document.getElementById("myInput");
    copyText.select();
    copyText.setSelectionRange(0, 99999);
    navigator.clipboard
      .writeText(copyText.value)
      .then(() => {
        alert("You have copied the colour " + copyText.value +" to your Clipboard");
      })
      .catch(() => {
        alert("something went wrong");
      });
}
